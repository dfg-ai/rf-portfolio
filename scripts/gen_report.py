#!/usr/bin/env python
"""Generate project report as .docx on Desktop — pure stdlib, no external deps."""
import zipfile, os, sys
from xml.etree import ElementTree as ET

DESKTOP = os.path.expanduser("~/Desktop")
W = "http://schemas.openxmlformats.org/wordprocessingml/2006/main"
R = "http://schemas.openxmlformats.org/officeDocument/2006/relationships"
CT = "http://schemas.openxmlformats.org/package/2006/content-types"

for uri, prefix in [(W, "w"), (R, "r"), (CT, "ct")]:
    ET.register_namespace(prefix, uri)


def tag(ns, name):
    return "{%s}%s" % (ns, name)


def para(text, bold=False, size=22, center=False, indent=0, spacing=120):
    """Create a w:p element."""
    p = ET.Element(tag(W, "p"))
    pPr = ET.SubElement(p, tag(W, "pPr"))
    if center:
        ET.SubElement(pPr, tag(W, "jc")).set(tag(W, "val"), "center")
    sp = ET.SubElement(pPr, tag(W, "spacing"))
    sp.set(tag(W, "after"), str(spacing))
    if indent:
        ET.SubElement(pPr, tag(W, "ind")).set(tag(W, "left"), str(indent))
    r = ET.SubElement(p, tag(W, "r"))
    rPr = ET.SubElement(r, tag(W, "rPr"))
    if bold:
        ET.SubElement(rPr, tag(W, "b"))
    ET.SubElement(rPr, tag(W, "sz")).set(tag(W, "val"), str(size))
    ET.SubElement(rPr, tag(W, "rFonts")).set(tag(W, "ascii"), "Calibri")
    ET.SubElement(rPr, tag(W, "rFonts")).set(tag(W, "hAnsi"), "Calibri")
    t = ET.SubElement(r, tag(W, "t"))
    t.set("{http://www.w3.org/XML/1998/namespace}space", "preserve")
    t.text = text
    return p


def build_docx(title, subtitle, sections):
    """Return a dict of path->bytes for the .docx zip."""
    # word/document.xml
    body = ET.Element(tag(W, "body"))
    body.append(para(title, bold=True, size=32, center=True, spacing=80))
    body.append(para(subtitle, size=20, center=True, spacing=300))

    for heading, lines in sections:
        body.append(para(heading, bold=True, size=24, spacing=120))
        for line in lines:
            if line == "":
                body.append(para("", size=4, spacing=20))
            elif line.startswith("  "):
                body.append(para(line.strip(), size=20, indent=480, spacing=40))
            elif line.startswith("★") or line.startswith(("[✓]", "[△]", "[✗]")):
                body.append(para(line, bold=True, size=20, spacing=60))
            else:
                body.append(para(line, size=20, spacing=60))

    body.append(para("Generated by Claude Code · HFSS Portfolio Project", size=16, center=True, spacing=200))

    document = ET.Element(tag(W, "document"))
    document.append(body)
    doc_xml = b'<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ET.tostring(document, "utf-8")

    # word/styles.xml
    styles = ET.Element(tag(W, "styles"))
    style = ET.SubElement(styles, tag(W, "style"))
    style.set(tag(W, "type"), "paragraph")
    style.set(tag(W, "styleId"), "Normal")
    ET.SubElement(style, tag(W, "name")).set(tag(W, "val"), "Normal")
    styles_xml = b'<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ET.tostring(styles, "utf-8")

    # [Content_Types].xml
    types = ET.Element(tag(CT, "Types"))
    ET.SubElement(types, tag(CT, "Default"), Extension="rels", ContentType="application/vnd.openxmlformats-package.relationships+xml")
    ET.SubElement(types, tag(CT, "Default"), Extension="xml", ContentType="application/xml")
    ct_xml = b'<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ET.tostring(types, "utf-8")

    # _rels/.rels
    rels = ET.Element(tag(R, "Relationships"))
    ET.SubElement(rels, tag(R, "Relationship"), Id="rId1",
                  Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument",
                  Target="word/document.xml")
    rels_xml = b'<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ET.tostring(rels, "utf-8")

    # word/_rels/document.xml.rels
    wrels = ET.Element(tag(R, "Relationships"))
    ET.SubElement(wrels, tag(R, "Relationship"), Id="rId1",
                  Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/styles",
                  Target="styles.xml")
    wrels_xml = b'<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n' + ET.tostring(wrels, "utf-8")

    return {
        "[Content_Types].xml": ct_xml,
        "_rels/.rels": rels_xml,
        "word/document.xml": doc_xml,
        "word/styles.xml": styles_xml,
        "word/_rels/document.xml.rels": wrels_xml,
    }


