def build_html(title, subtitle, sections):
    lines = ['<!DOCTYPE html><html><head><meta charset="utf-8">',
             f'<title>{title}</title><style>{CSS}</style></head><body>',
             f'<h1>{title}</h1>',
             f'<p class="sub">{subtitle}</p>']
    in_find = False
    for heading, items in sections:
        lines.append(f'<h2>{heading}</h2>')
        for item in items:
            if item == '':
                continue
            if item.startswith('★'):
                if not in_find:
                    lines.append('<div class="find">')
                    in_find = True
                lines.append(f'<p><b>{item}</b></p>')
            elif item.startswith('[✓]') or item.startswith('[△]') or item.startswith('[✗]'):
                cls = 'good' if '✓' in item else 'warn'
                lines.append(f'<p class="{cls}">{item}</p>')
            elif item.startswith(('  ', '\t', '  •')):
                lines.append(f'<p class="indent">{item.strip()}</p>')
            else:
                if in_find:
                    lines.append('</div>')
                    in_find = False
                lines.append(f'<p>{item}</p>')
        if in_find:
            lines.append('</div>')
            in_find = False
    lines.append('<p class="footer">Generated by Claude Code · HFSS Portfolio Project</p>')
    lines.append('</body></html>')
    return '\n'.join(lines)


