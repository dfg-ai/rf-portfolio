def make_html(report, output_path):
    """Generate a clean HTML file Word can open."""
    css = """
    body { font-family: 'Microsoft YaHei', Calibri, sans-serif; margin: 60px 80px; color: #1a1a1a; line-height: 1.7; }
    h1 { font-size: 22pt; text-align: center; color: #1a1a1a; margin-bottom: 4pt; }
    .subtitle { text-align: center; color: #666; font-size: 11pt; margin-bottom: 28pt; }
    h2 { font-size: 14pt; color: #2c5f2d; border-bottom: 2px solid #2c5f2d; padding-bottom: 4pt; margin-top: 24pt; }
    p, li { font-size: 11pt; margin: 4pt 0; }
    .findings { background: #f0f7f0; border-left: 4px solid #2c5f2d; padding: 8pt 14pt; margin: 10pt 0; }
    table { border-collapse: collapse; width: 100%; margin: 10pt 0; }
    td, th { border: 1px solid #ccc; padding: 5pt 10pt; font-size: 11pt; text-align: left; }
    th { background: #e8e8e8; font-weight: bold; }
    .footer { margin-top: 30pt; padding-top: 12pt; border-top: 1px solid #ddd; color: #999; font-size: 9pt; text-align: center; }
    """
    lines = [
        "<!DOCTYPE html><html><head><meta charset='utf-8'>",
        f"<title>{report['title']}</title>",
        f"<style>{css}</style></head><body>",
        f"<h1>{report['title']}</h1>",
        f"<p class='subtitle'>{report['subtitle']}</p>",
    ]
    for heading, items in report["sections"]:
        lines.append(f"<h2>{heading}</h2>")
        in_findings = False
        for item in items:
            if item == "":
                if in_findings:
                    lines.append("</div>")
                    in_findings = False
                continue
            if item.startswith("★"):
                if not in_findings:
                    lines.append("<div class='findings'>")
                    in_findings = True
                lines.append(f"<p><b>{item}</b></p>")
            elif item.startswith("  "):
                lines.append(f"<p style='margin-left:20pt'>{item.strip()}</p>")
            elif item.startswith(("[✓]", "[△]", "[✗]")):
                lines.append(f"<p><b>{item}</b></p>")
            else:
                if in_findings:
                    lines.append("</div>")
                    in_findings = False
                lines.append(f"<p>{item}</p>")
        if in_findings:
            lines.append("</div>")
    lines.append("<p class='footer'>Generated by Claude Code · HFSS Portfolio Project</p>")
    lines.append("</body></html>")

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    return output_path


# ── Try python-docx for .docx; fall back to warning ───────────────
def make_docx_fallback(report, path_html):
    """If python-docx installed, generate real .docx; else just note the HTML path."""
    try:
        from docx import Document
        from docx.shared import Pt, Inches, Cm, RGBColor
        from docx.enum.text import WD_ALIGN_PARAGRAPH
    except ImportError:
        print(f"NOTE: python-docx not available. Use the .html file instead (Word can open it).")
        print(f"HTML: {path_html}")
        return None

    doc = Document()
    # Page margins
    for section in doc.sections:
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(2.5)

    # Title
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(report["title"])
    run.bold = True
    run.font.size = Pt(20)

    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(report["subtitle"])
    run.font.size = Pt(10)
    run.font.color.rgb = RGBColor(0x66, 0x66, 0x66)

    doc.add_paragraph()  # spacer

    for heading, items in report["sections"]:
        doc.add_heading(heading, level=2)
        for item in items:
            if item == "":
                continue
            if item.startswith("★"):
                p = doc.add_paragraph()
                run = p.add_run(item)
                run.bold = True
                run.font.size = Pt(11)
            elif item.startswith("  "):
                p = doc.add_paragraph(item.strip())
                p.paragraph_format.left_indent = Cm(1)
            else:
                p = doc.add_paragraph(item)

    # Footer
    doc.add_paragraph()
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run("Generated by Claude Code · HFSS Portfolio Project")
    run.font.size = Pt(8)
    run.font.color.rgb = RGBColor(0x99, 0x99, 0x99)

    doc.save(path_html.replace(".html", ".docx"))
    return path_html.replace(".html", ".docx")


# ── Main ──────────────────────────────────────────────────────────
if __name__ == "__main__":
    name = sys.argv[1] if len(sys.argv) > 1 else "PatchAntenna_v1"
    report = REPORTS.get(name)
